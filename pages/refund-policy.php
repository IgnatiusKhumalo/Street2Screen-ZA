<h4>Overview</h4>
<p>Street2Screen ZA is committed to ensuring customer satisfaction. This policy outlines our procedures for refunds, cancellations, and returns to protect both buyers and sellers on our platform.</p>

<h4 class="mt-4">1. Cancellation Policy</h4>

<h5 class="mt-3">1.1 Buyer-Initiated Cancellations</h5>
<ul>
    <li><strong>Before Seller Confirmation (within 2 hours of order):</strong> Full refund available. No questions asked.</li>
    <li><strong>After Seller Confirmation:</strong> Cancellation requires seller approval. If approved, full refund minus 5% processing fee.</li>
    <li><strong>After Shipment:</strong> Order cannot be cancelled. Follow return process if item is unsatisfactory.</li>
</ul>

<h5 class="mt-3">1.2 Seller-Initiated Cancellations</h5>
<ul>
    <li><strong>Out of Stock:</strong> Seller must cancel within 24 hours. Buyer receives full refund within 3-5 business days.</li>
    <li><strong>Unable to Ship:</strong> Seller may cancel with valid reason. Buyer receives full refund.</li>
    <li><strong>Excessive Cancellations:</strong> Sellers with 3+ cancellations per month may face account suspension.</li>
</ul>

<h4 class="mt-4">2. Refund Policy</h4>

<h5 class="mt-3">2.1 Eligible for Full Refund</h5>
<ul>
    <li><strong>Item Not Received:</strong> If item not delivered within 14 days of expected delivery (COD) or 14 days after payment (PayFast)</li>
    <li><strong>Item Not as Described:</strong> Product significantly different from listing (wrong item, wrong color, wrong size, etc.)</li>
    <li><strong>Item Damaged/Defective:</strong> Product arrives damaged or defective (buyer must provide photo evidence within 48 hours of delivery)</li>
    <li><strong>Counterfeit/Fake Items:</strong> If item proven to be counterfeit or not authentic</li>
    <li><strong>Seller Non-Performance:</strong> Seller fails to ship within agreed timeframe</li>
</ul>

<h5 class="mt-3">2.2 Partial Refund Situations</h5>
<ul>
    <li><strong>Minor Defects:</strong> 25-50% refund if item has minor issues but is still usable</li>
    <li><strong>Missing Accessories:</strong> Partial refund for missing non-essential accessories</li>
    <li><strong>Late Delivery (not seller's fault):</strong> 10% compensation if delivery delayed beyond expected date (weather, courier issues)</li>
</ul>

<h5 class="mt-3">2.3 NOT Eligible for Refund</h5>
<ul>
    <li><strong>Change of Mind:</strong> Buyer simply changed mind (no defect, item as described)</li>
    <li><strong>Size/Fit Issues:</strong> Item doesn't fit (unless seller provided incorrect size chart)</li>
    <li><strong>Buyer's Remorse:</strong> Buyer no longer wants item</li>
    <li><strong>Used/Worn Items:</strong> Item has been used, worn, or tags removed (unless defective)</li>
    <li><strong>Late Filing:</strong> Refund request filed more than 7 days after delivery</li>
</ul>

<h4 class="mt-4">3. Return Process</h4>

<h5 class="mt-3">3.1 How to Request a Return</h5>
<ol>
    <li><strong>Step 1:</strong> Go to "My Orders" and find the order</li>
    <li><strong>Step 2:</strong> Click "Request Return/Refund" within 7 days of delivery</li>
    <li><strong>Step 3:</strong> Select reason and provide evidence (photos, videos, detailed description)</li>
    <li><strong>Step 4:</strong> Submit request. Seller has 48 hours to respond</li>
    <li><strong>Step 5:</strong> If approved, follow return shipping instructions</li>
    <li><strong>Step 6:</strong> Refund processed within 5-7 business days after seller confirms return</li>
</ol>

<h5 class="mt-3">3.2 Return Shipping Costs</h5>
<ul>
    <li><strong>Seller's Fault (defective, wrong item, etc.):</strong> Seller pays return shipping</li>
    <li><strong>Buyer's Fault (wrong size, change of mind):</strong> Buyer pays return shipping (if seller agrees to accept return)</li>
    <li><strong>Shipping Method:</strong> Returns must use same courier service used for delivery (trackable)</li>
</ul>

<h5 class="mt-3">3.3 Return Conditions</h5>
<p>Items must be returned in their original condition:</p>
<ul>
    <li>Unused and unworn (unless defective)</li>
    <li>Original packaging intact</li>
    <li>All tags, labels, and accessories included</li>
    <li>No signs of damage or misuse</li>
</ul>

<h4 class="mt-4">4. Refund Processing</h4>

<h5 class="mt-3">4.1 Refund Timeline</h5>
<ul>
    <li><strong>COD Orders (not yet delivered):</strong> Order cancelled, no payment made</li>
    <li><strong>PayFast Orders:</strong> Refund processed to original payment method within 5-7 business days</li>
    <li><strong>Bank Transfers:</strong> Buyer must provide bank details; refund within 3-5 business days</li>
</ul>

<h5 class="mt-3">4.2 Refund Methods</h5>
<ul>
    <li><strong>Credit/Debit Card:</strong> Refunded to original card (5-7 business days)</li>
    <li><strong>EFT/Bank Transfer:</strong> Direct deposit to buyer's account (3-5 business days)</li>
    <li><strong>Platform Credit:</strong> Instant credit to buyer's Street2Screen wallet (can be used for future purchases)</li>
</ul>

<h4 class="mt-4">5. Dispute Resolution</h4>

<h5 class="mt-3">5.1 When Buyer and Seller Disagree</h5>
<p>If buyer requests refund and seller rejects:</p>
<ol>
    <li><strong>Step 1:</strong> Buyer can escalate to "Dispute"</li>
    <li><strong>Step 2:</strong> Our moderators review evidence from both parties</li>
    <li><strong>Step 3:</strong> Moderator makes final decision within 7 business days</li>
    <li><strong>Step 4:</strong> Decision is binding and final</li>
</ol>

<h5 class="mt-3">5.2 Evidence Required</h5>
<p>Buyers must provide:</p>
<ul>
    <li>Clear photos/videos of the issue</li>
    <li>Screenshots of communication with seller</li>
    <li>Proof of delivery (if claiming non-delivery)</li>
    <li>Original product listing screenshots</li>
</ul>

<h4 class="mt-4">6. Special Cases</h4>

<h5 class="mt-3">6.1 Perishable Goods</h5>
<ul>
    <li>Food items cannot be returned</li>
    <li>Refund only if item arrived spoiled/rotten (photo evidence required within 2 hours of delivery)</li>
</ul>

<h5 class="mt-3">6.2 Custom/Personalized Items</h5>
<ul>
    <li>Custom-made items cannot be returned unless defective</li>
    <li>Seller must clearly state "no returns" in listing</li>
</ul>

<h5 class="mt-3">6.3 Digital Products (if applicable)</h5>
<ul>
    <li>No refunds on digital downloads after purchase</li>
    <li>Exception: If file is corrupted or cannot be accessed</li>
</ul>

<h4 class="mt-4">7. Platform Commission</h4>
<ul>
    <li><strong>Full Refund:</strong> Platform commission (10%) is refunded to seller</li>
    <li><strong>Partial Refund:</strong> Commission adjusted proportionally</li>
    <li><strong>Seller Cancellation (seller's fault):</strong> Seller pays commission as penalty</li>
</ul>

<h4 class="mt-4">8. Fraudulent Claims</h4>
<p><strong>Warning:</strong> Making false refund claims is a serious offense.</p>
<ul>
    <li>1st Offense: Warning + claim denied</li>
    <li>2nd Offense: 30-day account suspension</li>
    <li>3rd Offense: Permanent account ban</li>
</ul>

<h4 class="mt-4">9. Exceptions</h4>
<p>This policy does not apply to:</p>
<ul>
    <li>Items purchased during clearance sales marked "Final Sale"</li>
    <li>Seller-advertised "No Return" items (unless defective)</li>
    <li>Orders placed more than 90 days ago</li>
</ul>

